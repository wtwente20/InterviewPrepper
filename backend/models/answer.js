const mongoose = require('mongoose');

const answerSchema = new mongoose.Schema({
  response: {
    type: String,
    required: true,
    trim: true,
  },
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true,
  },
  questionId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Question',
    required: true,
  },
});

module.exports = mongoose.model('Answer', answerSchema);
